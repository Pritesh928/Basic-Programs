import java.util.*;

public class Sumof2 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter a no");
        int num1 = sc.nextInt();
        System.out.println("Enter another no");
        int num2 = sc.nextInt();
        int sum;
        sum = num1 + num2;
        System.out.println("The sum of 2 no is " + sum);
    }
}
