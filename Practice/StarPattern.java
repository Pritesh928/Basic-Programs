import java.util.*;

public class StarPattern {
    public static void main(String[] args) {
        //Stars using basic method..
        System.out.println("*");
        System.out.println("**");
        System.out.println("***");
        System.out.println("****");
        //Stars Using Escape Sequences..
        System.out.println("****\n***\n**\n*");
        System.out.println("   *\n  **\n ***\n****");
        System.out.println("****\n ***\n  **\n   *");
        

    }
}
